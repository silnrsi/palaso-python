class Name(object):
    def __init__(self, name = None, finalcomp = False):
        self.psname = name
        self.components = []
        self.ext = None
        self.cname = None
        self.GDLName = None
        if not name: return

        for comp in name.split("_"):
            base, dot, mod = comp.partition(".")
            if not base and mod: 
                base = dot + mod
                mod = None
            if re.match(r"^uni[0-9A-Fa-f]{4}", base):
                self.components.extend((int(x, 16), None) for x in re.findall(r"[0-9A-Fa-f]{4}", base))
                if mod:
                    self.components[-1] = (self.components[-1][0], mod)
            elif re.match(r"^u?[0-9A-Fa-f]{4,6}$", base):
                if base[0] == 'u':
                    self.components.append((int(base[1:], 16), mod))
                else:
                    self.components.append((int(base, 16), mod))
            elif base in agl:
                self.components.append((ord(agl[base]), mod))
            elif len(self.components):
                self.components[-1] = (self.components[-1][0], mod)
            else:
                self.components.append((base, mod))
        if not finalcomp or len(self.components) == 1:
            self.ext = self.components[-1][1]
            if self.ext:
                if self.components[-1][0]: 
                    self.components[-1] = (self.components[-1][0], None)
                else:
                    self.components.pop(-1)
        self.canonical()
        return

    def canonical(self):
        if self.cname: return self.cname
        res = ""
        if not len(self.components):
            self.cname = self.psname
            return self.cname
        for k in self.components:
            u = k[0]
            if not isinstance(u, (bytes, str)) and u:
                n = "%04X" % u
            else:
                n = u
            if not u:
                pass
            elif n in names:
                res += names[n]
            elif not res:
                res = "u" + n
            else:
                res += n
            if k[1]:
                res += "." + k[1]
            res += "_"
        self.cname = res[0:-1]
        if self.ext:
            self.cname += "." + self.ext
        return self.cname

    def GDL(self):
        if self.GDLName: return self.GDLName
        res = ""
        if not len(self.components):
            res = "g_" + self.psname.replace('.', '_')
            self.GDLName = re.sub(r"([A-Z])", lambda x: "_" + x.group(1).lower(), res)
            return self.GDLName
        for k in self.components:
            u = k[0]
            if not isinstance(u, (bytes, str)) and u:
                n = "%04X" % u
            else:
                n = u.replace('.', '_')

            if not u:
                pass
            elif n in names:
                if not res: res = "g_"
                res += re.sub("([A-Z])", lambda x: "_" + x.group(1).lower(), names[n])
            elif not res:
                res = "g" + n.lower()
            else:
                res += n.lower()
            if res and k[1]:
                res += "." + k[1].lower()
            res += "_"
        self.GDLName = res[0:-1]
        if self.ext:
            self.GDLName += "_" + self.ext.lower()
        return self.GDLName

    def head(self):
        res = Name()
        comps = list(self.components)
        if len(comps) and not self.ext:
            comps[-1] = (comps[-1], None)
        res.components = comps
        return res

